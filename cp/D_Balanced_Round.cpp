#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define mod 1000000007

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    ll ttt = 1;
    cin >> ttt;
    while (ttt--)
    {
        ll n, k;
        cin >> n >> k;
        vector<ll> arr(n);
        for (ll i = 0; i < n; i++)
        {
            cin >> arr[i];
        }
        sort(arr.begin(), arr.end());
        ll maxCnt = 1;
        ll cnt = 1;

        for (ll i = 1; i < n; i++)
        {
            if (abs(arr[i - 1] - arr[i]) <= k)
                cnt += 1;
            else
                cnt = 1;
            maxCnt = max(maxCnt, cnt);
        }

        cout << n - maxCnt << "\n";
    }
    return 0;
}
