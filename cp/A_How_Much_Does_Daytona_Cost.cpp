#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define mod 1000000007

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    ll ttt = 1;
    cin >> ttt;
    while (ttt--)
    {
        int n, k;
        cin >> n >> k;
        unordered_map<int, int> ump;
        for (int i = 0; i < n; i++)
        {
            int x;
            cin >> x;
            ump[x]++;
        }
        cout << ((ump[k] > 0) ? "YES\n" : "NO\n");
    }
    return 0;
}
