#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define mod 1000000007

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    ll ttt = 1;
    cin >> ttt;
    while (ttt--)
    {
        int n;
        cin >> n;
        vector<int> arr(n);
        for (int i = 0; i < n; i++)
        {
            cin >> arr[i];
        }

        vector<int> seq;
        seq.push_back(arr[0]);

        for (int i = 1; i < n; i++)
        {
            if (arr[i - 1] > arr[i])
            {
                seq.push_back(1);
            }
            seq.push_back(arr[i]);
        }
        cout << seq.size() << "\n";
        for (auto &s : seq)
            cout << s << " ";
        cout << "\n";
    }
    return 0;
}
