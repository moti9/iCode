#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define mod 1000000007

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    ll ttt = 1;
    cin >> ttt;
    while (ttt--)
    {
        int n;
        cin >> n;
        vector<int> arr(n);
        for (int i = 0; i < n; i++)
        {
            cin >> arr[i];
        }
        int cnt = 0;
        for (int i = 0; i < n;)
        {
            if (arr[i] != 0)
            {
                cnt += 1;
                while (i < n && arr[i] != 0)
                    i += 1;
            }
            else
            {
                i += 1;
            }
        }
        cout << cnt << "\n";
    }
    return 0;
}
