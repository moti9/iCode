#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define mod 1000000007

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    ll ttt = 1;
    cin >> ttt;
    while (ttt--)
    {
        int a, b;
        cin >> a >> b;
        int ans = -1;
        for (int m = min(a, b); m <= 1e6; m++)
        {
            if (m % a == m % b)
            {
                ans = m;
                break;
            }
        }
        cout << ans << "\n";
    }
    return 0;
}
