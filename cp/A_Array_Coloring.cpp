#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define mod 1000000007

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    ll ttt = 1;
    cin >> ttt;
    while (ttt--)
    {
        int n;
        cin >> n;
        int cnt_odd = 0, cnt_even = 0;
        for (int i = 0; i < n; i++)
        {
            int x;
            cin >> x;
            (x & 1) ? cnt_odd += 1 : cnt_even += 1;
        }
        if (cnt_odd & 1)
            cout << "NO\n";
        else
            cout << "YES\n";
    }
    return 0;
}
